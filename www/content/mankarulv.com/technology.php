	<h2>Segment rotation - how it works</h2>
	<a href="http://www.mankar.ca/pics/patent2.jpg" rel="lightbox[patent]" title="The patented Mantis system is adjustable between 10-55 cm coverage per spray head."><img class="pleft" src="http://www.mankar.ca/pics/thumbs/patent2.jpg" alt=""></a>
